import mongoose, { Schema } from "mongoose";

const listItemSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    time: {
      type: String,
      required: true,
    },
    isDone: {
      type: Boolean,
      default: false,
    },
  },
  { timestamps: true }
);
const ListedItem = mongoose.model("listeditems", listItemSchema);
export default ListedItem;
